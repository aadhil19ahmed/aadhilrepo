class ColorSelector {
    public static class ColorChooser{
        public static String chooseColor(float inputvalue){

            if((inputvalue>0)&&(inputvalue<10))
                return "#548eea";
            else if((inputvalue>10)&&(inputvalue<20))
                return "#a14bed";
            else if((inputvalue>20)&&(inputvalue<50))
                return "#e830cf";
            else if((inputvalue>50)&&(inputvalue<100)) {
                return "#e00644";
            }
            else if((inputvalue>100)&&(inputvalue<200))
                return "#1eea32";
            else if((inputvalue>200)&&(inputvalue<500))
                return "#d0d317";
            else if((inputvalue>500)&&(inputvalue<1000))
                return "#231f1e";
            else {
                return "#3ee8e8";
            }

        }
    }
}
